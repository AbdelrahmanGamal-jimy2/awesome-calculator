    private void calculate(String operation)
    {
        String currentValue = jTextField1.getText();
        CalculatorControler controler = CalculatorControler.getInstance();
        int indexOfOperator = 0;
        if(token.compareTo("") == 0)
        {
            if(operation.compareTo("(") == 0)
            {
                token = "(";
                return;
            }
            else if (operation.compareTo("!") == 0)
            {
                token = "!";
                changeFontAndsetTextFeild(currentValue + "!");
                return;
            }
            number = currentValue;
            token = operation;
            changeFontAndsetTextFeild(currentValue + operation);
        }
        else
        {
            String secondValue = "";
            switch(token)
            {
                case "+":
                    indexOfOperator = currentValue.lastIndexOf("+");
                    for(int i =indexOfOperator+1;i<currentValue.length();i++)
                    {
                        secondValue+= currentValue.charAt(i);
                    }
                    if(indexOfOperator == currentValue.length()-1 )
                    {
                        token = operation;
                        changeFontAndsetTextFeild(currentValue.substring(0, currentValue.length()-1) + operation);
                    }
                    else
                    {
                        number = "" + controler.addition(Float.parseFloat(number), Float.parseFloat(secondValue));
                        changeFontAndsetTextFeild(number + operation);
                        token = operation;
                    }
                    break;
                case "-":
                    indexOfOperator = currentValue.lastIndexOf("-");
                    for(int i =indexOfOperator+1;i<currentValue.length();i++)
                    {
                        secondValue+= currentValue.charAt(i);
                    }
                    if(indexOfOperator == currentValue.length()-1 )
                    {
                        token = operation;
                        changeFontAndsetTextFeild(currentValue.substring(0, currentValue.length()-1) + operation);
                    }
                    else
                    {
                        number = "" + controler.subtraction(Float.parseFloat(number), Float.parseFloat(secondValue));
                        changeFontAndsetTextFeild(number + operation);
                        token = operation;
                    }
                    break;
                case "X":
                    indexOfOperator = currentValue.lastIndexOf("X");
                    for(int i =indexOfOperator+1;i<currentValue.length();i++)
                    {
                        secondValue+= currentValue.charAt(i);
                    }
                    if(indexOfOperator == currentValue.length()-1 )
                    {
                        token = operation;
                        changeFontAndsetTextFeild(currentValue.substring(0, currentValue.length()-1) + operation);
                    }
                    else
                    {
                        number = "" + controler.multiplication(Float.parseFloat(number), Float.parseFloat(secondValue));
                        changeFontAndsetTextFeild(number + operation);
                        token = operation;
                    }
                    break;
                case "÷":
                    indexOfOperator = currentValue.lastIndexOf("÷");
                    for(int i =indexOfOperator+1;i<currentValue.length();i++)
                    {
                        secondValue+= currentValue.charAt(i);
                    }
                    if(Float.parseFloat(secondValue) == 0)
                    {
                        btnClear.doClick();
                        return;
                    }
                    if(indexOfOperator == currentValue.length()-1 )
                    {
                        token = operation;
                        changeFontAndsetTextFeild(currentValue.substring(0, currentValue.length()-1) + operation);
                    }
                    else
                    {
                        number = "" + controler.division(Float.parseFloat(number), Float.parseFloat(secondValue));
                        changeFontAndsetTextFeild(number + operation);
                        token = operation;
                    }
                    break;
                case "^":
                    indexOfOperator = currentValue.lastIndexOf("^");
                    for(int i =indexOfOperator+1;i<currentValue.length();i++)
                    {
                        secondValue+= currentValue.charAt(i);
                    }
                    if(indexOfOperator == currentValue.length()-1 )
                    {
                        token = operation;
                        changeFontAndsetTextFeild(currentValue.substring(0, currentValue.length()-1) + operation);
                    }
                    else
                    {
                        number = "" + controler.power(Float.parseFloat(number), Float.parseFloat(secondValue));
                        changeFontAndsetTextFeild(number + operation);
                        token = operation;
                    }
                    break;
                case "(":
                    indexOfOperator = currentValue.lastIndexOf("(");
                    for(int i =indexOfOperator+1;i<currentValue.length();i++)
                    {
                        secondValue+= currentValue.charAt(i);
                    }
                    if(Float.parseFloat(secondValue) == 90)
                    {
                        btnClear.doClick();
                        return;
                        
                    }
                    if(indexOfOperator == currentValue.length()-1 )
                    {
                        token = operation;
                        changeFontAndsetTextFeild(currentValue.substring(0, currentValue.length()-1) + operation);
                    }
                    else
                    {
                        number = "" + controler.tan(Float.parseFloat(secondValue));
                        if(operation.compareTo("(") == 0)
                        {
                            changeFontAndsetTextFeild("tan(" + number);
                            token = "(";
                        }
                        else
                        {
                            changeFontAndsetTextFeild(number + operation);
                            token = operation;
                        }
                        
                        
                    }
                    break;
                case "!":
                    indexOfOperator = currentValue.lastIndexOf("!");
                    for(int i =0;i<indexOfOperator;i++)
                    {
                        secondValue+= currentValue.charAt(i);
                    }
                    number = "" + controler.factorial(Float.parseFloat(secondValue));
                    changeFontAndsetTextFeild(number + operation);
                    token = operation;
            }
        }
    }
    private int numberOfChar(String s, char c)
    {
        int count = 0;
        for(int i =0;i<s.length();i++)
        {
            if(s.charAt(i) == c)
                count++;
        }
        return count;
    }
    private void changeFontAndsetTextFeild(String value)
    {
        if(value.length() >15 && (jTextField1.getFont().getSize() > 15)  )
        {
            Font font1 = new Font("Tahoma", Font.PLAIN, 40 - value.length());
            jTextField1.setFont(font1);
        }
        jTextField1.setText(value);
    }
